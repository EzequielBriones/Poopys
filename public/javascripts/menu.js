/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////VARIABLES////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////  DOM  //////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////
const menuBtn = document.querySelector(".nav__button");
const menuNav = document.querySelector(".nav");
const modOver = document.querySelector(".modal");

///////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////  NAV  //////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////
let menuOpen = false;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////FUNTIONS////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////// CALL MODAL FUNCTION ON BTN CLICK  /////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////

menuBtn.addEventListener("click", function () {
  modal();
});

menuNav.addEventListener("click", function () {
  modal();
});

///////////////////////////////////////////////////////////////////////////////////////
/////////////////////////// OPEN/CLOSE MODAL ON BTN CLICK /////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////

function modal() {
  if (!menuOpen) {
    menuNav.style.backgroundImage = "url(/images/menu-closed.svg)"; // cambia la imagen del nav cuando se abre
    modOver.classList.add("open-modal");
    menuOpen = true;
  } else {
    menuOpen = false;
    setTimeout(() => {
      menuNav.style.backgroundImage = "url(/images/menu-open.svg)"; // cambia la imagen del nav cuando se cierra
    }, 350);
    modOver.classList.remove("open-modal");
  }
}
